package conditionalStatements;

public class IfElse {
	public static void main(String[] args) {
		int age = 60;
		if (age > 60) {
			System.out.println("age is above 60");
		} else if (age < 60) {
			System.out.println("age is below 60");
		} else {
			System.out.println("age is equal to 60");
		}
	}
}
